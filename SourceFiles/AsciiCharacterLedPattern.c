unsigned int GetASCIIToSegmentPattern(unsigned char ASCII_Letter)
{
    unsigned int output;

    if(ASCII_Letter < TOTAL_LED_ASCII)
    {
        output = ASCIIToPatternArray[ASCII_Letter].CONVERSION_PATTERN;
    }
    else 
    {
        output = _NA_CHARACTER_PATTERN;
    }

    return output;
}

unsigned char GetHexValOfAscii(unsigned char Index_Of_ASCII_Letter)
{
    unsigned char output;

    if(Index_Of_ASCII_Letter < TOTAL_LED_ASCII)
    {
        output = ASCIIToPatternArray[Index_Of_ASCII_Letter].ASCII_LETTER;
    }
    else 
    {
        output = _NA_CHARACTER_PATTERN;
    }

    return output;
}